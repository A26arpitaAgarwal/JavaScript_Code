// while loop printing numbers from 0 to 4
// it will run until condition dont gets false
let i = 0;

while (i < 5) {
  console.log("The value of i is ${i}.");
  i++;
}


// do while loop
// runs atleast one time if condition is not true
let i = 0;

do {
  console.log("The value of i is ${i}.");
  i++;
} while (i < 5);
